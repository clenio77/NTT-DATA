lista = ['p','y','t','h','o','n']

lista[2:0] # da posição 2 pra frente
lista[:2] # até a posição 2 exclusiva
lista[1:3] # da posição 1 até a 3 exclusiva
lista[0:3:2] #da posição 0 até 3, pulando de 2 em 2
lista[::] # tudo padrão
lista[::-1] # traz a lista ao contrario(espelhado)

